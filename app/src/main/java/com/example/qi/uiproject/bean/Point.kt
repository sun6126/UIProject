package com.example.qi.uiproject.bean

import android.graphics.Color

class Point(private var x:Float,private var y:Float) {
    fun getX():Float{
        return x
    }

    fun setX(value:Float){
        x = value
    }

    fun getY():Float{
        return y
    }

    fun setY(value:Float){
        y = value
    }


}